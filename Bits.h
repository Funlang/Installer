/*========================================================================*/
#include	<alloc.h>


/*========================================================================*/
#define		_BITS_		0

#ifndef		_BIT_
#include	"bit.h"
#endif

#ifndef		false
#define		false		0
#define		true		1
typedef		int		bool;
#endif


/*========================================================================*/
bool	InitBits	();
bool	DeleteBits	();


/*========================================================================*/
Bit		*bits;


/*========================================================================*/
bool	InitBits	()
{
	int		i;

	do	bits=malloc(sizeof(Bit)*256);	/* alloc.h */
	while (!bits);

	for (i=0;i<256;i++)
	{
		bits[i].b32=0L;
		bits[i].len=0;
	}

	return true;
}

/*========================================================================*/
bool	DeleteBits	()
{
	free (bits);				/* alloc.h */

	return true;
}


